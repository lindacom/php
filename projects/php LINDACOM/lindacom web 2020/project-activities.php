      <h2>Business case</h2>
           <p>In Prince2 all projects must have a documented business justification. A business case is developed at the beginning of a project and provides the cost, benefits, expected benefits, risk and timescales. It confirms whether the project is desirable, viable and achievable.</p>
      <p>The business case is reviewed and updated as it develops and evolves and formally verified by the project board at each key decision point.</p>
       <h2>Project planning</h2>
      <ul>
  <li>Design a plan (project plan (high level), detailed level plans - stage plan, team plan (optional), exception plan)</li>
<li>Define and analyse the products</li>
<li>Identify activities and dependancies</li>
        <li>Prepare estimates</li>
         <li>Prepare a schedule</li>
        <li>Analyse risks to the plan</li>
           <li>Document and publish the plan</li>
</ul>  
<h2>Measuring performance</h2>
      <p>The six aspects of performance are</p>
      <ul>
  <li>Cost</li>
<li>Time</li>
<li>Quality</li>
        <li>Scope</li>
        <li>Benefits</li>
        <li>Risk</li>
</ul>
         <p>These aspects are measurable and can therefore be managed. Any of these aspects could change which would in turn affect the other aspects.</p>
      
      <h2>Risk</h2>
      <p><strong>Risk</strong> is an uncertain event or set of events that should it occur will have an effect on achievement of objectives.</p>
      <p><strong>Issue</strong> is something that has or will happen. (If a risk is not dealt with it becomes an issue.)</p>
        
        <p>Prince2 minimum requirements in relation to riskare</p>
       <ul>
  <li>Define risk management approach</li>
<li>Maintain some form of risk register</li>
<li>Ensure that project risks are identified (lessons log), assessed (probability and proximity), managed (plan mitigations) and reviewed (establish owner) throughout the project lifecycle</li>
        <li>Use lessons to inform risk identification and management</li>
        </ul>
      <p>Make sure there is good communication between risk owners, project manager and stakeholders</p>
