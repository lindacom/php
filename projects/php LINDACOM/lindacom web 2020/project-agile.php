        <h1>Agile Project Management</h1>
     <p class="lead">Agile is often used for software projects when the full scope of the project is not known</p>  
      <p>Agile has a very strong focus on principle. Product descriptions (sometimes written as user stories), quality criteria and quality tolerances can be prioritised to provide flexibility in what is being delivered. Benefits are delivered early, change is handled dynamically and reduced risk because delivery is incremental.</p>
      <p>Prince2 management stages can be aligned with a series of sprints or releases to support a fail fast and learn fast environment.</p>
       <h2>Agile</h2>
      <ul>
        <li>Uses a timeboxed and iterative approach to delivery and or</li>
         <li>Uses a collection of techniques (e.g. daily stand up meetings, sprints and user stories) and or</li>
           <li>Usesthe SCRUM framework</li>
        </ul>
           <h3>Fundamental values and principles of agile</h3>
        <h4>Values</h4>
       <ul>
        <li>Individuals and interactions over processes and tools.</li>
         <li>Working software over comprehensive documentation.</li>
           <li>Customer collaboration over contract negotiation.</li>
          <li>Responding to change over following a plan.</li>
        </ul>
      <h4>Principles</h4>
      <p>There are 12 principles behind the Agile manifesto </p>
      <ol>
         <li>Satisfy the customer</li>
         <li>Welcome change</li>
           <li>Deliver frequently</li>
          <li>Working together</li>
         <li>Trust people to get the job done</li>
         <li>Face to face conversation</li>
           <li>Working software primary measure of progress</li>
          <li>Maintain a constant pace</li>
         <li>Continuous attention to technical excellence and good design</li>
         <li>Simplicity</li>
           <li>Self organising teams</li>
          <li>Reflect on how to become more effective and adjust behaviour accordingly</li>
        </ol>
         <h4>Behaviours</h4>
      <p>Behaviours need to function smoothly for agile to operate in the most effective way.</p>
       <ul>
        <li>Transparency</li>
         <li>Collaboration</li>
           <li>Rich communication</li>
          <li>Self organisation</li>
          <li>Exploration</li>
        </ul>
           <h2>Waterfall</h2>
      <p>A development approach that is linear and sequential with distinct goals for each phase of development.  Earlier stages are not revisited.</p>
          <h2>SCRUM</h2>
      <p>Scrum is a framework within which people can address complex, adaptive problems, while productively and creatively delivering products of the highest possible value.  The scrum team consists of product owner who owns the backlog and prioritises items, development team, scrum master who helps the team to follow agile principles without controlling what they do.</p>
      <h2>Kanban</h2>
      <img src="images/kanban.jpg" width="250px" style="float:left; padding:10px;"><p>A way to improve flow through visualisation and controlling work in progress.  The focus is on getting work done as soon as possible.  Kanban boards can be used to show items ready, build, test and deploy (done or ready). Work in progress limits are defined by the team. Kanban is not a project management framework and a project manager role is not defined in it.  Kanban can be used on a project as part of an approach to delivering products.</p>
